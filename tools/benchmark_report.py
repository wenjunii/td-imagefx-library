"""Render TouchDesigner runtime benchmark samples as Markdown."""

from __future__ import annotations

import argparse
import json
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--input", type=Path, default=ROOT / "docs" / "benchmark-data.json")
    parser.add_argument("--output", type=Path, default=ROOT / "docs" / "benchmarks.md")
    parser.add_argument("--check", action="store_true")
    args = parser.parse_args()
    payload = json.loads(args.input.read_text(encoding="utf-8"))
    samples = sorted(
        payload.get("samples", []),
        key=lambda item: (
            float(item["gpu_ms"]) if isinstance(item.get("gpu_ms"), (int, float)) else -1.0,
            float(item.get("cpu_submission_ms", 0.0)),
        ),
        reverse=True,
    )
    lines = [
        "# TouchDesigner runtime benchmark report",
        "",
        "This report is generated by `python tools/benchmark_report.py`. Results are hardware- and resolution-specific, not universal performance promises.",
        "",
        f"- TouchDesigner build: `{payload.get('touchdesigner_build', 'unknown')}`",
        f"- GPU: `{payload.get('gpu', 'unknown')}`",
        f"- Resolution: `{payload.get('resolution', 'unknown')}`",
        f"- Frames per sample: `{payload.get('frames_per_sample', 'unknown')}`",
        f"- Method: {payload.get('method', 'unknown')}",
        "",
        "> " + payload.get("gpu_timing_note", "GPU timing availability varies by TouchDesigner build and graphics driver."),
        "",
        "| Effect version | Model | Cost class | Passes | Median GPU ms | CPU submit ms | GPU memory | 60 Hz GPU budget |",
        "|---|---:|---:|---:|---:|---:|---:|---:|",
    ]
    for sample in samples:
        gpu_ms = sample.get("gpu_ms")
        if isinstance(gpu_ms, (int, float)):
            gpu_text = f"{float(gpu_ms):.3f}"
            budget_text = f"{float(gpu_ms) / 16.6667 * 100.0:.1f}%"
        else:
            gpu_text = "n/a"
            budget_text = "n/a"
        memory_mib = float(sample.get("gpu_memory_bytes", 0)) / (1024.0 * 1024.0)
        lines.append(
            f"| `{sample['id']}@{sample.get('version', 'unknown')}` | {sample.get('model', 'unknown')} | {sample.get('gpu_cost', 'unknown')} | "
            f"{int(sample.get('passes', 1))} | {gpu_text} | {float(sample.get('cpu_submission_ms', 0.0)):.3f} | "
            f"{memory_mib:.2f} MiB | {budget_text} |"
        )
    content = "\n".join(lines) + "\n"
    if args.check:
        if not args.output.is_file() or args.output.read_text(encoding="utf-8") != content:
            print(f"Benchmark report is stale: {args.output}")
            return 1
    else:
        args.output.parent.mkdir(parents=True, exist_ok=True)
        args.output.write_text(content, encoding="utf-8", newline="\n")
    print(f"Benchmark report contains {len(samples)} samples")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
